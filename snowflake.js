function generateSnowflakeBuffer(gl, twgl) {
    const array = {
        // 4 vertices
        position: [
            // 0, 0, 0,
            0, 1, 0, 
            0.866,0.5,0,
            0.866,-0.5,0,
            0, -1, 0,
            -0.866,-0.5,0,
            -0.866,0.5,0, 
            
            0, 2, 0, 
            1.732,1,0,
            1.732,-1,0,
            0, -2, 0,
            -1.732,-1,0,
            -1.732,1,0,

            1.5, 2.598, 0,
            3, 0, 0,
            1.5, -2.59, 0, 
            -1.5, -2.59, 0,
            -3, 0, 0,
            -1.5, 2.59, 0,

        ],
        indices: [
            0, 1, 2,
            4, 5, 0,
            2, 3, 4, 
            // 0, 2, 4,

            6, 7, 8,
            10, 11, 6,
            8, 9, 10,


            0, 1, 12,
            1, 2, 13, 
            2, 3, 14,
            3, 4, 15, 
            4, 5, 16,
            5, 0, 17,

        ],
    };
    const buffer = twgl.createBufferInfoFromArrays(gl, array);
    return buffer;
}
